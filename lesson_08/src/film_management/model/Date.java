package film_management.model;

import java.time.LocalDate;
import java.time.format.DateTimeFormatter;

public class Date {
    private int day,month, year;

    public Date() {
    }

    public Date(int day, int month, int year) {
        this.day = day;
        this.month = month;
        this.year = year;
    }

    @Override
    public String toString() {
        return "Date{" +
                "day=" + day +
                ", month=" + month +
                ", year=" + year +
                '}';
    }
}
